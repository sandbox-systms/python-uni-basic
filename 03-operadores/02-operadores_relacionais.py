"""Topico 2: Operadores relacionais.

Comparacoes geram valores booleanos, fundamentais para validacoes,
regras de negocio e condicionais.
"""

def main():
    """Exibe comparacoes relacionais e seus resultados booleanos."""
    idade = 18

    print("idade == 18:", idade == 18)
    print("idade != 20:", idade != 20)
    print("idade > 16:", idade > 16)
    print("idade < 21:", idade < 21)
    print("idade >= 18:", idade >= 18)
    print("idade <= 17:", idade <= 17)


if __name__ == "__main__":
    main()
