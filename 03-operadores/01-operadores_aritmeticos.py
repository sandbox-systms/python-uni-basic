"""Topico 1: Operadores aritmeticos.

Esses operadores sao usados em calculadoras, precificacao, estatisticas
e praticamente qualquer rotina que trabalhe com numeros.
"""

def main():
    """Exibe exemplos simples de operadores aritmeticos."""
    a = 10
    b = 3

    print("Soma:", a + b)
    print("Subtracao:", a - b)
    print("Multiplicacao:", a * b)
    print("Divisao:", a / b)
    print("Modulo:", a % b)
    print("Potencia:", a ** b)


if __name__ == "__main__":
    main()
