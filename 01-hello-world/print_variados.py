# Diferentes formas de usar o print()

# Print com aspas duplas
print("Aspas duplas")

# Print com aspas simples
print('Aspas simples')

# Print com múltiplos valores
print("Nome:", "Maria", "Idade:", 25)

# Print com separador personalizado
print("Maçã", "Banana", "Laranja", sep=" - ")

# Print com end personalizado (evita quebra de linha)
print("Primeira linha", end=" | ")
print("continuação")

# Print de números e expressões
print(10 + 5)
print(2 * 3 + 4)

# Print vazio
print()

# Print com formatação f-string
nome = "Carlos"
print(f"Olá, {nome}!")

# Print com quebras de linha \n
print("Linha 1\nLinha 2\nLinha 3")

# Print com tabulação \t
print("Coluna 1\tColuna 2\tColuna 3")
