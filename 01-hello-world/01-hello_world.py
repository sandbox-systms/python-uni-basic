"""Topico 1: Hello World.

Este arquivo demonstra o uso inicial de `print()` para exibir mensagens
no terminal. Em contexto real, esse recurso ajuda a depurar scripts,
informar status e confirmar resultados rapidamente.
"""

def main():
    """Executa os exemplos iniciais de saida no terminal."""
    # A funcao print() envia a mensagem para a saida do programa.
    print("Hello, World!")

    # Strings podem ser escritas com aspas simples ou duplas.
    print('Ola, Python!')

    # Varias chamadas de print() ajudam a exibir passos separados.
    print("Ola")
    print("Mundo")

    # Multiplos argumentos sao unidos por espaco por padrao.
    print("Olá", "Mundo", "!")


if __name__ == "__main__":
    main()
