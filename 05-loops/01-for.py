"""Topico 1: Loop for.

`for` e indicado quando a quantidade de iteracoes e conhecida ou quando
se deseja percorrer uma colecao inteira.
"""

def main():
    """Executa uma contagem simples com for."""
    for numero in range(1, 6):
        print("Contagem:", numero)


if __name__ == "__main__":
    main()
