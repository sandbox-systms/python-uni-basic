"""Topico 2: Estrutura if e else.

Use esta estrutura quando houver dois caminhos mutuamente exclusivos,
como sucesso ou falha, permitido ou negado.
"""

def main():
    """Executa um exemplo simples com if e else."""
    numero = 7

    if numero % 2 == 0:
        print("O numero e par.")
    else:
        print("O numero e impar.")


if __name__ == "__main__":
    main()
