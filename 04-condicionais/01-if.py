"""Topico 1: Estrutura if.

`if` executa um bloco somente quando a condicao e verdadeira.
Esse padrao aparece em alertas, validacoes e verificacoes simples.
"""

def main():
    """Executa um exemplo simples com a estrutura if."""
    idade = 20

    if idade >= 18:
        print("Voce e maior de idade.")


if __name__ == "__main__":
    main()
