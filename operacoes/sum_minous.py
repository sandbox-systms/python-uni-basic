# Nesta seção, vamos explorar os tipos de operadores em Python. Os operadores são usados para representar valores algebricos
# ou seja, eles permitem realizar operações matemáticas, lógicas e de comparação entre valores.

# Operadores Aritméticos:
# - Adição (+): Soma dois valores.
# - Subtração (-): Subtrai um valor de outro.
# - Multiplicação (*): Multiplica dois valores.
# - Divisão (/): Divide um valor por outro.
# - Divisão Inteira (//): Divide um valor por outro e retorna o resultado inteiro.
# - Módulo (%): Retorna o resto da divisão entre dois valores.
# - Exponenciação (**): Eleva um valor à potência de outro.

# Exemplo de uso de operadores aritméticos:
a = 10
b = 3 

print(a + b)  # Saída: 13
print(a - b)  # Saída: 7
print(a * b)  # Saída: 30
print(a / b)  # Saída: 3.3333333333333335
print(a // b) # Saída: 3
print(a % b)  # Saída: 1
print(a ** b) # Saída: 1000
