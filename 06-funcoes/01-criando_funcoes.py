"""Topico 1: Criando funcoes.

Funcoes reduzem repeticao e agrupam responsabilidades. Este exemplo
mostra o formato minimo de definicao e chamada.
"""

def saudacao():
    """Exibe uma mensagem padrao de boas-vindas."""
    print("Bem-vindo ao estudo de funcoes!")


def main():
    """Executa a chamada da funcao de saudacao."""
    saudacao()


if __name__ == "__main__":
    main()
